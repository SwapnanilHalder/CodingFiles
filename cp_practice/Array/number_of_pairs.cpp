    //Function to count number of pairs such that x^y is greater than y^x.
    
    long long countPairs(int X[], int Y[], int m, int n)
    {
       //Your code here
       vector<double> x (X, X + m);
       vector<double> y (Y, Y+n);
       long long count=0;
       for(int i=0; i<m; i++) {
           x[i] = pow(  (double)(x[i]) , (1.0 / (double)x[i] )  );
       }
       pv(x);
       sort(x.begin(), x.end());
       pv(x);
       for(int i=0; i<n; i++) {
           y[i] = pow((double)y[i] , ((double)1 / (double)y[i] )  );
           
           
           long long idx = (upper_bound(x.begin(), x.end(), y[i]) - x.begin() );
           count += (m - idx);
       }
       pv(y);
       return count;
    }
