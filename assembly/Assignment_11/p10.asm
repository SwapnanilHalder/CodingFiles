.MODEL SMALL
.STACK 100H
.CODE
MAIN PROC
    MOV CX,5
    PUSH CX
    CALL PRINT
    MOV AH,76
    INT 21H
MAIN ENDP
PRINT PROC
    PUSH BP
    MOV BP,SP
    MOV CX,WORD PTR [BP+4]
    JCXZ EXIT
    MOV AH,2
    MOV DL,65
L:
    INT 21H
    INC DL
    LOOP L
    MOV CX,WORD PTR [BP+4]
    DEC CX
    PUSH CX
    CALL PRINT
EXIT:
    POP BP
    RET 2
PRINT ENDP
END MAIN