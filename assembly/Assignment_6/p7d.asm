.MODEL SMALL
.DATA
	ARR DB 100 DUP('?')
.CODE
	MOV AX,@DATA
	MOV DS,AX
	LEA SI,ARR
	MOV CX,5
	MOV AH,1
	READ:
		INT 21H
		MOV [SI],AL
		INC SI
		LOOP READ
	MOV AH,9
	MOV CX,5
	BIG_LOOP:
		LEA DX,ARR
		MOV [SI],36
		MOV BL,CL
		OUTPUT:
			DEC BL
			INT 21H
			INC DX
			CMP BL,0
			JG OUTPUT
		DEC SI
		LOOP BIG_LOOP
	MOV AH,76
	INT 21H
END
