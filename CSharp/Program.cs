using System; 
  
class forLoopDemo 
{ 
    public static void Main() 
    { 
        // for loop begins when x=1 
        // and runs till x <=4 
        for (int x = 1; x <= 4; x++) {
            Console.WriteLine("GeeksforGeeks"); }
        string p = Console.ReadLine();
        Console.WriteLine(p);
    }  
}